public class Operadores3 {

    public static void main(String[] args) {
        
        //classe Operadores.java
        int numero = 5;
		
        //Imprimindo o numero negativo com o operador de negação (-)
        System.out.println(- numero);
        
        //Imprimindo o numero novamente e o resultado é 5
        System.out.println(numero);
        //O que isso quer dizer? O operador de negação não altera o valor na variável

        //Para alterar o número na variável usando o operador de negação
        numero = - numero;

       //Para volta-lo ao valor positivo, multipica por -1
       numero = numero * -1;

    }  
}
