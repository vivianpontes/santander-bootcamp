public class Operadores4 {
    public static void main(String[] args) {
        //Operadores para incrementar um número

        int numero = 5;

        // Para aumentar em 1 numero, temos duas opções
        // 'numero = numero + 1;' OU
        numero++;
        System.out.println(numero);

        // Outra opção é
        System.out.println(++numero);
        // Resultado = 

        // Atenção! Para incrementrar no 'print', adicionar o '++' antes do número
        System.out.println(numero++);
        //Esse valor não será a mesma coisa do 'print' de cima
    }
}
